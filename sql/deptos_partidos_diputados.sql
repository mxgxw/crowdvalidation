FROM diputados;

/* Insertar partidos */
INSERT INTO partidos (nombre)
SELECT DISTINCT partido
FROM diputados;

/* Actualizar llaves de departamentos */
UPDATE diputados
    INNER JOIN departamentos
        ON diputados.departamento = departamentos.nombre
SET departamento_id = departamentos.id;
    

/* Actualizar llaves de partidos */
UPDATE diputados
    INNER JOIN partidos
        ON diputados.partido = partidos.nombre
SET partido_id = partidos.id;

/* Eliminar columnas innecesarias de diputados */
ALTER TABLE diputados
  DROP COLUMN departamento,
  DROP COLUMN partido;

/* Marcando columnas de FK como no nulas */
ALTER TABLE diputados
    MODIFY departamento_id TINYINT NOT NULL,
    MODIFY partido_id TINYINT NOT NULL;

/* Agregando FKs */
ALTER TABLE diputados
    ADD CONSTRAINT fk_departamentos_diputados FOREIGN KEY (departamento_id)
        REFERENCES departamentos(id)
        ON DELETE NO ACTION,
    ADD CONSTRAINT fk_partidos_diputados FOREIGN KEY (partido_id)
        REFERENCES partidos(id)
        ON DELETE NO ACTION
